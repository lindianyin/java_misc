package com.nfl.kfb.mapper.inv;

import java.util.Date;

public class InvBak {
	private int id;
	private String appId;
	private Date time;
	private int state;
	private String friId;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAppId() {
		return appId;
	}
	public void setAppId(String appId) {
		this.appId = appId;
	}
	public Date getTime() {
		return time;
	}
	public void setTime(Date time) {
		this.time = time;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	public String getFriId() {
		return friId;
	}
	public void setFriId(String friId) {
		this.friId = friId;
	}

	
	
}
