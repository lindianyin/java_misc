public class WeekAchieve {
	
	private int week;
	private int aidx;
	private int achTId;
	private int achId;
	private int achValue;
	private int rewardItemId;
	private int rewardItemCnt;
	
	public int getWeek() {
		return week;
	}
	
	public void setWeek(int week) {
		this.week = week;
	}
	
	public int getAidx() {
		return aidx;
	}
	
	public void setAidx(int aidx) {
		this.aidx = aidx;
	}
	
	public int getAchTId() {
		return achTId;
	}
	
	public void setAchTId(int achTId) {
		this.achTId = achTId;
	}
	
	public int getAchId() {
		return achId;
	}
	
	public void setAchId(int achId) {
		this.achId = achId;
	}
	
	public int getAchValue() {
		return achValue;
	}
	
	public void setAchValue(int achValue) {
		this.achValue = achValue;
	}
	
	public int getRewardItemId() {
		return rewardItemId;
	}
	
	public void setRewardItemId(int rewardItemId) {
		this.rewardItemId = rewardItemId;
	}
	
	public int getRewardItemCnt() {
		return rewardItemCnt;
	}
	
	public void setRewardItemCnt(int rewardItemCnt) {
		this.rewardItemCnt = rewardItemCnt;
	}

}
