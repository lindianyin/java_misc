public class AdminSubtract {
	
	private String appId;
	private int subGold;
	private int subBall;
	private int subPunch;
	
	public String getAppId() {
		return appId;
	}
	
	public void setAppId(String appId) {
		this.appId = appId;
	}

	public int getSubGold() {
		return subGold;
	}

	public void setSubGold(int subGold) {
		this.subGold = subGold;
	}

	public int getSubBall() {
		return subBall;
	}

	public void setSubBall(int subBall) {
		this.subBall = subBall;
	}

	public int getSubPunch() {
		return subPunch;
	}

	public void setSubPunch(int subPunch) {
		this.subPunch = subPunch;
	}

}
